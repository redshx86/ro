; --------------------------------------------------------------------------------------------------

#include <avr/io.h>
#include "macro.inc"

; --------------------------------------------------------------------------------------------------

.global TIMER2_OVF_vect
.global t_rtc_sec

; --------------------------------------------------------------------------------------------------

; 2s async timer interrupt
TIMER2_OVF_vect:
	sbi		GPIOR0,1					; TASK_FLAG_SET(RTC_TICK_FLAG)
	reti

; --------------------------------------------------------------------------------------------------

.section ".bss"

t_rtc_sec:		.word 0, 0

; --------------------------------------------------------------------------------------------------
